// GetAllCategories returns all product categories
func (s *productService) GetAllCategories(ctx context.Context, empty *pb.Empty) (*pb.CategoriesMessageResponse, error) {
    // Get the database and collection
    db := s.db.Database("productdb")
    categoriesCollection := db.Collection("categories")

    // Find all categories
    cursor, err := categoriesCollection.Find(ctx, bson.M{})
    if err != nil {
        return nil, fmt.Errorf("failed to find categories: %v", err)
    }
    defer cursor.Close(ctx)

    var categories []*pb.Category
    for cursor.Next(ctx) {
        var category bson.M
        if err := cursor.Decode(&category); err != nil {
            return nil, fmt.Errorf("failed to decode category: %v", err)
        }

        // Convert MongoDB document to protobuf message
        pbCategory := &pb.Category{
            CategoryId: category["_id"].(primitive.ObjectID).Hex(),
            Name:       category["name"].(string),
        }
        categories = append(categories, pbCategory)
    }

    if err := cursor.Err(); err != nil {
        return nil, fmt.Errorf("cursor error: %v", err)
    }

    return &pb.CategoriesMessageResponse{
        Categories: categories,
    }, nil
}

